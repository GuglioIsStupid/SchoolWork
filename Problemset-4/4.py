# This program gets the average of 4 jump lengths
# and displays the average
jump1 = float(input("Enter the length of the first jump: "))
jump2 = float(input("Enter the length of the second jump: "))
jump3 = float(input("Enter the length of the third jump: "))
jump4 = float(input("Enter the length of the fourth jump: "))
total = jump1 + jump2 + jump3 + jump4
average = total / 4

print("The total of your four jumps is", total)
print("The average of your four jumps is", average)